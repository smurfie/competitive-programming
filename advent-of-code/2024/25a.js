  function solve(input) {
    let lines = Utils.read(input);

    let holes = [];
    let keys = [];
    let mat = [];
    for (let i = 0; i < lines.length; i++) {
      let line = lines[i];
      if (line.length === 0) {
        if (mat[0][0] === ".") {
          keys.push(calcElement(mat, "."));
        } else {
          holes.push(calcElement(mat, "#"));
        }
        mat = [];
      } else {
        mat.push(line.split(""));
      }
    }
    if (mat[0][0] === ".") {
      keys.push(calcElement(mat, "."));
    } else {
      holes.push(calcElement(mat, "#"));
    }
    let sum = 0;
    for (let key of keys) {
      for (let hole of holes) {
        if (fit(key, hole)) {
          sum++;
        }
      }
    }

    return sum;
  }

  function calcElement(mat, element) {
    let arr = new Array(mat[0].length).fill(0);
    for (let line of mat) {
      for (let i = 0; i < line.length; i++) {
        if (line[i] === element) {
          arr[i]++;
        }
      }
    }
    return arr;
  }

  function fit(key, hole) {
    for (let i = 0; i < key.length; i++) {
      if (key[i] < hole[i]) {
        return false;
      }
    }
    return true;
  }

