  function solve(input) {
    const UP = 0;
    const RIGHT = 1;
    const DOWN = 2;
    const LEFT = 3;
    let lines = Utils.read(input);
    let matrix = lines.map((i) => i.split(""));
    let x = 0;
    let y = 0;
    let found = false;
    let dir = UP;
    let visitedWithDir = {};
    let visited = {};
    let sum = 0;

    for (let i = 0; i < matrix.length && !found; i++) {
      for (let j = 0; j < matrix[i].length && !found; j++) {
        if (matrix[i][j] === "^") {
          x = i;
          y = j;
          found = true;
        }
      }
    }

    while (
      x >= 0 &&
      x < matrix.length &&
      y >= 0 &&
      y < matrix[0].length &&
      !visitedWithDir[hash(x, y, dir)]
    ) {
      visitedWithDir[hash(x, y, dir)] = true;
      if (!visited[hash(x, y, "")]) {
        sum++;
        visited[hash(x, y, "")] = true;
      }
      if (dir === UP) {
        if (x > 0 && matrix[x - 1][y] === "#") {
          dir = RIGHT;
        } else {
          x--;
        }
      } else if (dir === RIGHT) {
        if (y < matrix[0].length - 1 && matrix[x][y + 1] === "#") {
          dir = DOWN;
        } else {
          y++;
        }
      } else if (dir === DOWN) {
        if (x < matrix.length - 1 && matrix[x + 1][y] === "#") {
          dir = LEFT;
        } else {
          x++;
        }
      } else if (dir === LEFT) {
        if (y > 0 && matrix[x][y - 1] === "#") {
          dir = UP;
        } else {
          y--;
        }
      }
    }

    return sum;
  }

