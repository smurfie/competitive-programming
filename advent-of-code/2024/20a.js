  function solve(input, saved) {
    let lines = Utils.read(input);
    let matrix = lines.map((i) => i.split(""));

    let xE;
    let yE;
    let xS;
    let yS;

    for (let i = 0; i < matrix.length; i++) {
      for (let j = 0; j < matrix[i].length; j++) {
        if (matrix[i][j] === "S") {
          xS = i;
          yS = j;
        }

        if (matrix[i][j] === "E") {
          xE = i;
          yE = j;
        }
      }
    }

    // First we calculate the distance of each point from the end
    let queue = [[xE, yE, 0]];
    let distances = {};
    while (queue.length > 0) {
      let [x, y, d] = queue.shift();
      if (distances[x + "," + y] === undefined) {
        distances[x + "," + y] = d;
        let neighbors = [
          [x + 1, y],
          [x - 1, y],
          [x, y + 1],
          [x, y - 1],
        ];
        for (let [xN, yN] of neighbors) {
          if (matrix[xN][yN] !== "#") {
            queue.push([xN, yN, d + 1]);
          }
        }
      }
    }

    // Now let's find how many paths there are from the start that going through only a wall
    // "saves" us at least saved steps
    let distance = distances[xS + "," + yS];
    let paths = 0;
    queue = [[xS, yS, 0]];
    let visited = {};

    while (queue.length > 0 && distance - queue[0][2] > saved) {
      let [x, y, d] = queue.shift();
      if (!visited[x + "," + y]) {
        visited[x + "," + y] = true;

        // TO discover a new Path: distance - (d + 2 + distace[jumpX + "," + jumpY]) >= saved
        // distance - d -2 - distance[jumpX + "," + jumpY] >= saved

        tmp = distance - d - 2;
        if (
          distances[x - 1 + "," + (y - 1)] !== undefined &&
          tmp - distances[x - 1 + "," + (y - 1)] >= saved
        ) {
          paths++;
        }
        if (
          distances[x + 1 + "," + (y - 1)] !== undefined &&
          tmp - distances[x + 1 + "," + (y - 1)] >= saved
        ) {
          paths++;
        }
        if (
          distances[x - 1 + "," + (y + 1)] !== undefined &&
          tmp - distances[x - 1 + "," + (y + 1)] >= saved
        ) {
          paths++;
        }
        if (
          distances[x + 1 + "," + (y + 1)] !== undefined &&
          tmp - distances[x + 1 + "," + (y + 1)] >= saved
        ) {
          paths++;
        }
        if (
          x >= 2 &&
          distances[x - 2 + "," + y] !== undefined &&
          tmp - distances[x - 2 + "," + y] >= saved
        ) {
          paths++;
        }
        if (
          y >= 2 &&
          distances[x + "," + (y - 2)] !== undefined &&
          tmp - distances[x + "," + (y - 2)] >= saved
        ) {
          paths++;
        }
        if (
          x < matrix.length - 2 &&
          distances[x + 2 + "," + y] !== undefined &&
          tmp - distances[x + 2 + "," + y] >= saved
        ) {
          paths++;
        }
        if (
          y < matrix[0].length - 2 &&
          distances[x + "," + (y + 2)] !== undefined &&
          tmp - distances[x + "," + (y + 2)] >= saved
        ) {
          paths++;
        }

        let neighbors = [
          [x + 1, y],
          [x - 1, y],
          [x, y + 1],
          [x, y - 1],
        ];
        for (let [xN, yN] of neighbors) {
          if (matrix[xN][yN] !== "#") {
            queue.push([xN, yN, d + 1]);
          }
        }
      }
    }

    return paths;
  }

