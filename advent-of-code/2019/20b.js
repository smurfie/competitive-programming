  function solve(input) {
    var map = input.split("\n").map((i) => i.split(""));
    var portals = {};
    var gates = {};
    for (var i = 0; i < map.length; i++) {
      for (var j = 0; j < map[i].length; j++) {
        if (isLetter(map, i, j) && isLetter(map, i + 1, j)) {
          var s = map[i][j] + map[i + 1][j];
          if (!gates[s]) gates[s] = [];
          if (isPath(map, i + 2, j)) {
            gates[s].push([i + 2, j]);
          } else {
            gates[s].push([i - 1, j]);
          }
          if (gates[s].length == 2) {
            var gate1 = gates[s][0][0] + "," + gates[s][0][1];
            var gate2 = gates[s][1][0] + "," + gates[s][1][1];
            portals[gate1] = gates[s][1];
            portals[gate2] = gates[s][0];
          }
        }
        if (isLetter(map, i, j) && isLetter(map, i, j + 1)) {
          var s = map[i][j] + map[i][j + 1];
          if (!gates[s]) gates[s] = [];
          if (isPath(map, i, j + 2)) {
            gates[s].push([i, j + 2]);
          } else {
            gates[s].push([i, j - 1]);
          }
          if (gates[s].length == 2) {
            var gate1 = gates[s][0][0] + "," + gates[s][0][1];
            var gate2 = gates[s][1][0] + "," + gates[s][1][1];
            portals[gate1] = gates[s][1];
            portals[gate2] = gates[s][0];
          }
        }
      }
    }

    var startX = gates["AA"][0][0];
    var startY = gates["AA"][0][1];
    var endX = gates["ZZ"][0][0];
    var endY = gates["ZZ"][0][1];

    return minPath(
      [Utils.duplicate(map)],
      startX,
      startY,
      0,
      endX,
      endY,
      0,
      portals,
      map
    );
  }

  function minPath(
    map,
    startX,
    startY,
    startZ,
    endX,
    endY,
    endZ,
    portals,
    originalMap
  ) {
    var q = [[startX, startY, startZ, 0]];
    while (q.length > 0) {
      var pos = q.shift();
      var [x, y, z, n] = pos;
      if (x == endX && y == endY && z == endZ) return n;
      if (isPath(map[z], x, y) && z < 1000) {
        // Limit the recursivity to 1000
        map[z][x][y] = "#";
        q.push([x - 1, y, z, n + 1]);
        q.push([x + 1, y, z, n + 1]);
        q.push([x, y - 1, z, n + 1]);
        q.push([x, y + 1, z, n + 1]);
        var portal = portals[x + "," + y];
        if (portal) {
          if (
            x == 2 ||
            y == 2 ||
            x == originalMap.length - 3 ||
            y == originalMap[x].length - 3
          ) {
            if (z > 0) q.push([portal[0], portal[1], z - 1, n + 1]);
          } else {
            if (z == map.length - 1) {
              map.push(Utils.duplicate(originalMap));
            }
            q.push([portal[0], portal[1], z + 1, n + 1]);
          }
        }
      }
    }
    return 0;
  }

  function isLetter(map, i, j) {
    return (
      i >= 0 &&
      i < map.length &&
      j >= 0 &&
      j < map[i].length &&
      map[i][j] != " " &&
      map[i][j] != "." &&
      map[i][j] != "#"
    );
  }

  function isPath(map, i, j) {
    return (
      i >= 0 &&
      i < map.length &&
      j >= 0 &&
      j < map[i].length &&
      map[i][j] == "."
    );
  }

